package Day1;

public class Topic_1_Hello {
    public static void main(String[] args) {
        // How to print something to the console
        System.out.println("Hello Java");

        // Concatenation and difference between print and println
        System.out.println("Test-0 " + "Test-1"); // println adds a new line after printing
        System.out.print("Test-2");               // print does NOT add a new line
    }
}
